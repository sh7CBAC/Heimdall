let hooked = false;
const installed = new Set<string>();

function bundleFor(language?: string): Record<string, string> {
  return String(language || '').toLowerCase().startsWith('fa') ? FA : EN;
}

function install(language?: string): void {
  const lang = language || i18next.language || 'en-US';
  if (installed.has(lang)) return;
  i18next.addResourceBundle(lang, NS, bundleFor(lang), true, false);
  installed.add(lang);
}

export function ensurePgAdminI18n(): void {
  if (!hooked) {
    i18next.on('initialized', () => ensurePgAdminI18n());
    i18next.on('languageChanged', lng => install(lng));
    hooked = true;
  }

  if (!i18next.isInitialized) return;

  [i18next.language, ...(i18next.languages || []), 'en-US', 'en', 'fa', 'fa-IR', 'fa_IR']
    .filter(Boolean)
    .forEach(lang => install(lang));
}

ensurePgAdminI18n();
